	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				saishimingcheng: '',
				fengmiantupian: '',
				zhuangtai: '',
				lunci: '',
				zhudui: '',
				kedui: '',
				saishididian: '',
				bisaishijian: '',
				},
				zhuangtaiOptions: [],
				zhuangtaiIndex: 0,
				// 登陆User信息
				user: {},
                                ro:{
                                   saishimingcheng : false,
                                   fengmiantupian : false,
                                   zhuangtai : false,
                                   lunci : false,
                                   zhudui : false,
                                   kedui : false,
                                   saishididian : false,
                                   bisaishijian : false,
                                },
			}
		},
		components: {
			wPicker,
            xiaEditor
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取User信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取


			// 自定义下拉框值
			this.zhuangtaiOptions = "to be started,Started,Over".split(',')

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新Operation
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`saichengxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='saishimingcheng'){
					this.ruleForm.saishimingcheng = obj[o];
					this.ro.saishimingcheng = true;
					continue;
					}
					if(o=='fengmiantupian'){
					this.ruleForm.fengmiantupian = obj[o];
					this.ro.fengmiantupian = true;
					continue;
					}

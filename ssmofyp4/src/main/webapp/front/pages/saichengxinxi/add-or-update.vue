					this.ro.zhudui = true;
					continue;
					}
					if(o=='kedui'){
					this.ruleForm.kedui = obj[o];
					this.ro.kedui = true;
					continue;
					}
					if(o=='saishididian'){
					this.ruleForm.saishididian = obj[o];
					this.ro.saishididian = true;
					continue;
					}
					if(o=='bisaishijian'){
					this.ruleForm.bisaishijian = obj[o];
					this.ro.bisaishijian = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
